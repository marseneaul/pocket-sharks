function parseSprite(lines: string[]): SpriteData {
  const height = lines.length;
  const width = lines[0]?.length || 64;
  const pixels: number[][] = [];

  for (let y = 0; y < height; y++) {
    const row: number[] = [];
    const line = lines[y] || '';
    for (let x = 0; x < width; x++) {
      const char = line[x] || '.';
      switch (char) {
        case '#': row.push(1); break; // Darkest
        case '@': row.push(2); break; // Mid
        case 'o': row.push(3); break; // Lightest
        case 'O': row.push(3); break; // Lightest (alias)
        default: row.push(0); break;  // Transparent
      }
    }
    pixels.push(row);
  }

  return { width, height, pixels };
}

const spriteCache: Map<string, SpriteData> = new Map();

export function getSprite(creatureId: number, isFront: boolean): SpriteData {
  const key = `${creatureId}-${isFront ? 'front' : 'back'}`;

  if (!spriteCache.has(key)) {
    const spriteData = SHARK_SPRITES[creatureId] || DEFAULT_SHARK;
    const lines = isFront ? spriteData.front : spriteData.back;
    spriteCache.set(key, parseSprite(lines));
  }

  return spriteCache.get(key)!;
}

export function drawSprite(sprite: SpriteData, x: number, y: number, flipX: boolean = false): void {
  const ctx = getContext();
  // GBC sprite palette: transparent + 3 visible colors
  const colors = [null, DMG_PALETTE.SPRITE_DARK, DMG_PALETTE.SPRITE_MID, DMG_PALETTE.SPRITE_LIGHT];

  for (let row = 0; row < sprite.height; row++) {
    for (let col = 0; col < sprite.width; col++) {
      const srcCol = flipX ? sprite.width - 1 - col : col;
      const colorIndex = sprite.pixels[row][srcCol];
      if (colorIndex > 0) {
        ctx.fillStyle = colors[colorIndex]!;
        ctx.fillRect(Math.floor(x + col), Math.floor(y + row), 1, 1);
      }
    }
  }
}

export function drawCreatureSprite(creatureId: number, x: number, y: number, isFront: boolean): void {
  const sprite = getSprite(creatureId, isFront);
  drawSprite(sprite, x, y, false);
}
